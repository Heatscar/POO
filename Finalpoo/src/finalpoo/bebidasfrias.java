    private void butbebfringresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butbebfringresarActionPerformed
        //boton ingresar 
        
        Finalpoo articulo = new Finalpoo(bebfrnom.getText(),bebfrprecio.getText(),bebfrcan.getText(),bebfrdet.getText());
        lista.add(articulo);
    
        mostrar(); 
        
    }//GEN-LAST:event_butbebfringresarActionPerformed
    public void mostrar(){
       
    Object matriz [][] = new Object [lista.size()][4];//filas,columnas
    for (int i = 0; i<lista.size(); i++){
        matriz[i][0]= lista.get(i).getNombre();
        matriz[i][1]= lista.get(i).getPrecio();
        matriz[i][2]= lista.get(i).getCantidad();
        matriz[i][3]= lista.get(i).getDetalles();
       
    }
    tabnom.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String [] {
                "Nombre", "Precio", "Cantidad", "Detalles"
            }
        ));
}
